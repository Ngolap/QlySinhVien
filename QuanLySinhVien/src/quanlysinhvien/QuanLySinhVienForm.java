    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        // cap nhat: 1/ Dua du lieu tu bang len FORM 2/ Sua tren FORM 
        //           3/ Luu xong lai: them vaf xoa SV cu
        
        if (btnCapNhat.getText().equals("Cập nhật")){
            int hangCanSua = tbStudents.getSelectedRow();
            Student svCanSua = list.get(hangCanSua);
            txtHoTen.setText( svCanSua.name );
            txtDiem.setText( "" + svCanSua.marks  );
            btnCapNhat.setText("SAVE");
        } else {
            // ghi len dia
            Student svDaSua = new Student();
            svDaSua.name = txtHoTen.getText(); // ten da sửa
            svDaSua.marks = Double.parseDouble( txtDiem.getText()) ; // diem da sua
            svDaSua.major = (String) cbNganh.getSelectedItem();
            list.set( tbStudents.getSelectedRow(), svDaSua  );
            fillToTable();
            saveArrayListToFile()
                    ;
            btnCapNhat.setText("Cập nhật");
        }
                
        
        
        
        //JOptionPane.showMessageDialog(this, "Sua xong, chon THEM, roi XOA sv cu");
        
    }//GEN-LAST:event_btnCapNhatActionPerformed

