    public void fillToTable(){ 
        // dua ds sinh vien vao BANG
        DefaultTableModel model = (DefaultTableModel) tbStudents.getModel(); // b1: lay MODEL
        model.setNumRows(0); // b2: Xoa bang
        for(Student s: list){   // b3: tao ra cac hang, noi vao bang
            Object[] row = new Object[]{ s.name, s.marks,s.major, s.getGrade(), s.isBonus() };
            model.addRow(row);
        }
    
    }
    
