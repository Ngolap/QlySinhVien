    public void addStudent(){ 
        // tao moi 1 SV lays tu FORM
        Student s = new Student();
        s.name  = txtHoTen.getText();
        s.marks = Double.parseDouble( txtDiem.getText() ); 
        s.major = (String) cbNganh.getSelectedItem();
        list.add(s); // dua vao danh sach list
        txtHocLuc.setText(s.getGrade());
        chkThuong.setSelected(s.isBonus() );
    }
